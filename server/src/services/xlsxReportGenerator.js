/* ───── RPT-06: Asset Utilisation ───── */

async function generateAssetUtilisationXLSX(data, filePath, user) {
  const wb = createWorkbook();
  addInfoSheet(wb, { title: 'Skip Asset Utilisation', period: data.period, generatedBy: user?.full_name });

  const ws = wb.addWorksheet('Summary');
  ws.columns = [{ width: 30 }, { width: 20 }];
  ws.addRow(['Total Skips', data.totalSkips]);
  ws.addRow(['Average per Event', data.avgPerEvent]);
  ws.addRow([]);

  // By type
  const startRow = ws.rowCount + 1;
  ws.addRow(['Skip Type', 'Count']);
  styleHeaderRow(ws, startRow);
  let rowNum = startRow + 1;
  for (const t of data.byType) {
    ws.addRow([t.type, t.count]);
    styleDataRow(ws, rowNum++);
  }

  ws.addRow([]);
  const topStart = ws.rowCount + 1;
  ws.addRow(['Asset Label', 'Usage Count']);
  styleHeaderRow(ws, topStart);
  rowNum = topStart + 1;
  for (const a of data.topAssets) {
    ws.addRow([a.label, a.count]);
    styleDataRow(ws, rowNum++);
  }

  await wb.xlsx.writeFile(filePath);
}

